router.beforeEach((to, from, next) => {
  if (to.matched.some((item) => item.meta.login)) { // 如果父子路由所有meta的login存在说明需要登陆
    let info = router.app.$local.fetch('DYYS')
    if (info && info.login) { // 已经登录
      next()
    } else {
      router.push({
        path: '/Login',
        query: {redirect: to.path}
      })
    }
  } else {
    next()
  }
})

